public class ComputeMethods {

public double fToC(double degreesF){
    double degreesC = ((5/9) * (degreesF - 32));
    return(degreesC);

}


public double hypotenuse(int a, int b){
    double hypo = Math.sqrt((a * a) + (b * b)) ; 
    return(hypo);

}






}

